    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
      
        String s = label_timer.getText();
        
        
        int temp=0;
        lap_mili_stop=lap_mili_start+mili;
        
        if(lap_mili_stop>100){
            temp=1;
            lap_mili_stop-=100;
        }
        
        lap_sec_stop=lap_sec_start+sec+temp;
        
        temp=0;
        if(lap_sec_stop>60){
            temp=1;
            lap_sec_stop-=60;
        }
        
        lap_min_stop=lap_min_start+min+temp;
        
        temp=0;
        if(lap_min_stop>60){
            temp=1;
            lap_min_stop-=60;
        }
        
        lap_hour_stop=lap_hour_start+hour+temp;
        
        String str_start = lap_hour_start + ":" + lap_min_start + ":" + lap_sec_start + ":" + lap_mili_start;
        
        String str_stop = lap_hour_stop + ":" + lap_min_stop + ":" + lap_sec_stop + ":" + lap_mili_stop;
        
        str[i++]= "Lap " + i +" " + str_start + " " + str_stop + " " + s.substring(0);
        
        lap_hour_start=lap_hour_stop;
        lap_min_start=lap_min_stop;
        lap_sec_start=lap_sec_stop;
        lap_mili_start=lap_mili_stop;
        
        
        
        DefaultListModel kuchbhi = new DefaultListModel();
        
        for(int j=0;j<i;j++){
        
        kuchbhi.addElement(str[j]);
        }
        
        list_timer.setModel(kuchbhi);
        
    }//GEN-LAST:event_jButton3ActionPerformed

